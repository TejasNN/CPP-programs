////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//																			                                           
//	Problem statement :	Write a program which accept one number from user and toggle 7th bit of that number. Return modified number.
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

#include<iostream>
using namespace std;

typedef unsigned int UINT;

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//	Function name :		ToggleBit
//	Input :				unsigned integer
//	Output :			unsigned integer
// 	Description :		Returns modified number after toggling the 7th bit. 
// 	Author :			Tejas Nandakumar Nagvekar
// 	Date :				07/06/2023
// 
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

UINT ToggleBit(UINT iNo)
{
    UINT iResult = 0;
    UINT iMask = 0X00000040;

    iResult = iNo ^ iMask;

    return iResult;
}

/////////////////////////////////////////////////////////////////////////////////
//	Entry point function													   //
/////////////////////////////////////////////////////////////////////////////////

int main()
{
    UINT iValue = 0;
    UINT iRet = 0;

    cout<<"Enter a number : "<<"\n";
    cin>>iValue;

    iRet = ToggleBit(iValue);

    cout<<"The modified number is : "<<iRet<<"\n";

    return 0;
}
